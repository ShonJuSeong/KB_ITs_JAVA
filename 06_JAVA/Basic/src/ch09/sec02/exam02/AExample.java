package ch09.sec02.exam02;

public class AExample {
    public static void main(String[] args) {
    //A 객체 생성
    A a = new A();


    //A 인스턴스 메소드 호출
    a.useB();
   }
}

//A a = new A();
//A : 클래스 A의 타입(자료형). 위에서 정의한 A 클래스.
//a : 변수 이름. A 클래스의 인스턴스 참조 변수.
//= : 대입 연산자. 오른쪽에서 만든 객체를 왼쪽 변수에 저장.
//new : 객체를 힙 메모리에 새로 생성한다는 의미.
//A() : 클래스 A의 생성자 호출. 생성자가 실행되며 객체가 메모리에 생성됨.
//✔️ 이 줄은 클래스 A의 객체를 생성하고, 참조 변수 a에 할당하는 코드입니다.

//a.useB();
//a : 위에서 만든 A 클래스의 인스턴스(객체).
//. : 멤버 접근 연산자. 객체의 필드나 메서드에 접근할 때 사용.
//useB() : 클래스 A 내부에 정의된 인스턴스 메서드. 내부 클래스 B를 생성하고 사용하는 코드가 담겨 있음.
//(); : 메서드를 호출하는 괄호. 매개변수가 없기 때문에 빈 괄호 사용.
//✔️ 이 줄은 A 클래스 내부의 useB() 메서드를 실행시킵니다. 내부적으로 다음이 실행됩니다:
//내부 클래스 B의 인스턴스를 생성 → new B()
//field1 값 출력 → 1
//method1() 실행 → "B-method1 실행" 출력
//정적 필드 field2 출력 → 2
//정적 메서드 method2() 실행 → "B-method2 실행" 출력


//A a = new A(); 실행 → A 객체 생성됨.
//a.useB(); 실행:

//내부 클래스 B 생성 →              B-생성자 실행 출력
//인스턴스 필드 field1 출력 →        1
//인스턴스 메서드 method1 실행 →     B-method1 실행
//정적 필드 field2 출력 →           2
//정적 메서드 method2 실행 →        B-method2 실행